package pt1_college_management;

/**
 *
 * @author moraman
 */
public abstract class Member {

    protected String id;
    protected String firstName;
    protected String lastName;

    public Member() {
    }

    public Member(String id, String firstName, String lastName) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public abstract void addSubject(String subject);

    public abstract boolean searchSubject(String subject);

    public abstract String showData();
}
